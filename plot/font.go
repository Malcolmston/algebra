func init() {
	for r, rows := range glyphRows {
		var cols [glyphW]uint8
		for row := 0; row < glyphH; row++ {
			line := rows[row]
			for col := 0; col < glyphW; col++ {
				if col < len(line) && line[col] == '#' {
					cols[col] |= 1 << uint(row)
				}
			}
		}
		glyph5x7[r] = cols
	}
}

// textWidth returns the pixel width of s when rendered at the given integer
// scale, including inter-character spacing but excluding trailing spacing.
func textWidth(s string, scale int) int {
	if len(s) == 0 {
		return 0
	}
	return (len([]rune(s))*glyphAdv - 1) * scale
}

// textHeight returns the pixel height of a single line of text at scale.
func textHeight(scale int) int { return glyphH * scale }
