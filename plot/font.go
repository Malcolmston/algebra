// This file implements a tiny 5x7 bitmap font used to draw text (titles, axis
// labels, tick labels and the legend) directly into the raster PNG output. The
// SVG backend uses real <text> elements instead, so this font only affects PNG
// rendering.
//
// Each glyph is authored as seven strings of five characters, where '#' marks
// a lit pixel and any other rune marks background. At package initialisation
// the rows are packed into column-major bit masks in glyph5x7 so that drawing
// is a simple bit test. Glyphs are 5 pixels wide and 7 pixels tall; drawing
// advances 6 pixels per character (one pixel of spacing).

