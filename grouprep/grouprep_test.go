func TestDirectProductOrder(t *testing.T) {
	g := DirectProduct(CyclicGroup(2), CyclicGroup(3))
	if g.Order() != 6 {
		t.Fatalf("|C2×C3| = %d, want 6", g.Order())
	}
	// C2×C3 ≅ C6 is cyclic, hence abelian with an element of order 6.
	if !g.IsAbelian() {
		t.Errorf("C2×C3 should be abelian")
	}
	maxOrder := 0
	for i := 0; i < g.Order(); i++ {
		if o := g.ElementOrder(i); o > maxOrder {
			maxOrder = o
		}
	}
	if maxOrder != 6 {
		t.Errorf("max element order = %d, want 6", maxOrder)
	}
}

func equalInts(a, b []int) bool {
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
