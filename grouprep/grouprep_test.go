func TestCharacterTables(t *testing.T) {
	tables := []struct {
		name string
		ct   *CharacterTable
		dims []int
	}{
		{"C4", CharacterTableCyclic(4), []int{1, 1, 1, 1}},
		{"V4", CharacterTableKleinFour(), []int{1, 1, 1, 1}},
		{"D3", CharacterTableDihedral(3), []int{1, 1, 2}},
		{"D4", CharacterTableDihedral(4), []int{1, 1, 1, 1, 2}},
		{"D5", CharacterTableDihedral(5), []int{1, 1, 2, 2}},
		{"D6", CharacterTableDihedral(6), []int{1, 1, 1, 1, 2, 2}},
		{"S3", CharacterTableSymmetric(3), []int{1, 1, 2}},
		{"S4", CharacterTableSymmetric(4), []int{1, 1, 2, 3, 3}},
		{"Q8", CharacterTableQuaternion(), []int{1, 1, 1, 1, 2}},
	}
	for _, tc := range tables {
		t.Run(tc.name, func(t *testing.T) {
			ct := tc.ct
			if !ct.IsComplete() {
				t.Errorf("%s: table not complete (Σd² = %d, |G| = %d)",
					tc.name, ct.SumOfSquaresOfDims(), ct.Group().Order())
			}
			if got := ct.Dimensions(); !equalInts(got, tc.dims) {
				t.Errorf("%s: dims = %v, want %v", tc.name, got, tc.dims)
			}
			if !ct.RowOrthogonality(1e-7) {
				t.Errorf("%s: row orthogonality fails", tc.name)
			}
			if !ct.ColumnOrthogonality(1e-7) {
				t.Errorf("%s: column orthogonality fails", tc.name)
			}
			if ct.SumOfSquaresOfDims() != ct.Group().Order() {
				t.Errorf("%s: Σd² = %d, want %d", tc.name,
					ct.SumOfSquaresOfDims(), ct.Group().Order())
			}
		})
	}
}

func TestRegularDecomposition(t *testing.T) {
	// The regular representation contains each irreducible with multiplicity
	// equal to its degree.
	cases := []*CharacterTable{
		CharacterTableSymmetric(3),
		CharacterTableSymmetric(4),
		CharacterTableQuaternion(),
		CharacterTableDihedral(4),
	}
	for _, ct := range cases {
		reg := RegularCharacter(ct.Group())
		mult := ct.Decompose(reg)
		for i, m := range mult {
			if m != ct.Dimensions()[i] {
				t.Errorf("%s: irr %d multiplicity %d, want %d",
					ct.Group().Name(), i, m, ct.Dimensions()[i])
			}
		}
	}
}

func TestNaturalRepDecomposition(t *testing.T) {
	// The natural permutation rep of S_n is trivial ⊕ standard.
	for n := 3; n <= 4; n++ {
		ct := CharacterTableSymmetric(n)
		nat := NaturalRepSymmetric(n).Character()
		mult := ct.Decompose(nat)
		if mult[0] != 1 {
			t.Errorf("S%d: trivial multiplicity %d, want 1", n, mult[0])
		}
		total := 0
		for _, m := range mult {
			if m < 0 {
				t.Errorf("S%d: negative multiplicity %d", n, m)
			}
			total += m
		}
		// trivial + one (n-1)-dimensional standard = 2 constituents.
		if total != 2 {
			t.Errorf("S%d: total constituents %d, want 2", n, total)
		}
	}
}

func TestTensorCharacter(t *testing.T) {
	// χ_{V⊗W} = χ_V · χ_W.
	ct := CharacterTableSymmetric(4)
	sign := ct.Irreducible(1)
	std := ct.Irreducible(3)
	prod := TensorCharacters(sign, std)
	// standard ⊗ sign is the other 3-dimensional irreducible (index 4).
	other := ct.Irreducible(4)
	if !prod.ApproxEqual(other, 1e-7) {
		t.Errorf("sign⊗standard = %v, want %v", prod, other)
	}
}

func TestDualCharacter(t *testing.T) {
	// For C5 the dual of χ_k is χ_{5-k}; characters are complex conjugates.
	r := CyclicRep(5, 2)
	dual := r.Dual().Character()
	conj := ConjugateCharacter(r.Character())
	if !dual.ApproxEqual(conj, 1e-7) {
		t.Errorf("dual character != conjugate:\n%v\n%v", dual, conj)
	}
}

func TestFrobeniusReciprocity(t *testing.T) {
	// H = rotation subgroup C4 embeds in G = D4 via the identity index map on
	// rotations 0..3.
	G := DihedralGroup(4)
	H := CyclicGroup(4)
	emb := []int{0, 1, 2, 3}

	ctG := CharacterTableDihedral(4)
	ctH := CharacterTableCyclic(4)

	for hi := 0; hi < ctH.NumIrreducibles(); hi++ {
		psi := ctH.Irreducible(hi)
		ind, err := InduceCharacter(G, H, emb, psi)
		if err != nil {
			t.Fatalf("InduceCharacter: %v", err)
		}
		for gi := 0; gi < ctG.NumIrreducibles(); gi++ {
			chi := ctG.Irreducible(gi)
			lhs := InnerProduct(G, ind, chi)   // 〈Ind ψ, χ〉_G
			res := RestrictCharacter(chi, emb) // Res χ to H
			rhs := InnerProduct(H, psi, res)   // 〈ψ, Res χ〉_H
			if cmplx.Abs(lhs-rhs) > 1e-7 {
				t.Errorf("Frobenius reciprocity fails for H-irr %d, G-irr %d: %v vs %v",
					hi, gi, lhs, rhs)
			}
		}
	}
}

func TestInducedIsCharacter(t *testing.T) {
	G := DihedralGroup(4)
	H := CyclicGroup(4)
	emb := []int{0, 1, 2, 3}
	ctG := CharacterTableDihedral(4)
	ctH := CharacterTableCyclic(4)

	psi := ctH.Irreducible(1)
	ind, err := InduceCharacter(G, H, emb, psi)
	if err != nil {
		t.Fatalf("InduceCharacter: %v", err)
	}
	if !IsCharacter(G, ind, ctG.Irreducibles(), 1e-7) {
		t.Errorf("induced character is not a genuine character")
	}
	// Induced degree = [G:H]·deg(psi) = 2·1 = 2.
	if d := CharacterDimension(ind); d != 2 {
		t.Errorf("induced degree = %d, want 2", d)
	}
}

func TestRootOfUnity(t *testing.T) {
	if !ApproxEqualC(RootOfUnity(4, 1), complex(0, 1), tol) {
		t.Errorf("RootOfUnity(4,1) = %v, want i", RootOfUnity(4, 1))
	}
	if !ApproxEqualC(RootOfUnity(2, 1), complex(-1, 0), tol) {
		t.Errorf("RootOfUnity(2,1) = %v, want -1", RootOfUnity(2, 1))
	}
	// Sum of all n-th roots of unity is 0.
	var s complex128
	for k := 0; k < 6; k++ {
		s += RootOfUnity(6, k)
	}
	if cmplx.Abs(s) > tol {
		t.Errorf("sum of 6th roots = %v, want 0", s)
	}
}

func TestDihedralRep2DCharacter(t *testing.T) {
	// χ(r^b) = 2cos(2πhb/n) and χ(reflection) = 0.
	n, h := 4, 1
	r := DihedralRep2D(n, h)
	ch := r.Character()
	for b := 0; b < n; b++ {
		want := 2 * math.Cos(2*math.Pi*float64(h)*float64(b)/float64(n))
		if !ApproxEqualC(ch[b], complex(want, 0), 1e-7) {
			t.Errorf("χ(r^%d) = %v, want %v", b, ch[b], want)
		}
		// Reflections have index n+b.
		if !ApproxEqualC(ch[n+b], 0, 1e-7) {
			t.Errorf("χ(reflection %d) = %v, want 0", b, ch[n+b])
		}
	}
}

