func TestNewRepValidation(t *testing.T) {
	g := CyclicGroup(3)
	// A valid rep built from matrices.
	r := CyclicRep(3, 1)
	mats := make([]Matrix, g.Order())
	for i := 0; i < g.Order(); i++ {
		mats[i] = r.Matrix(i)
	}
	if _, err := NewRep(g, mats, tol); err != nil {
		t.Errorf("NewRep on valid data: %v", err)
	}
	// Break the homomorphism.
	mats[1] = Matrix{{2}}
	if _, err := NewRep(g, mats, tol); err == nil {
		t.Errorf("NewRep should reject a non-homomorphism")
	}
}

